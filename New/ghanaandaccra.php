<p><img class="floatright imgsettings imggh2" alt="Image of the sunset in Ghana" src="http://www.volunteerwithdunk.com/wp-content/uploads/2014/05/ws-sunset.jpg" /><h3><strong>Central Region</strong></h3><br>
While in the Central region, make your way to the <strong>Kakum National Park</strong> for a Canopy walk and experience the tropical forest at unprecedented heights, after which you can go see the crocodiles at <strong>Hans Botel Cottage</strong>.  A visit of the <strong>Cape Coast Castle</strong> or the <strong>Elmina Castle</strong> will provide you with an unforgettable learning experience of the slave trade era of the Gold Coast. <br><br>
After a wonderful day of exploring in Cape Coast, enjoy a relaxing and lively evening at <strong>Oasis Beach Resort</strong>, a small resort next to the Cape Coast Castle that provides good food, cheap rooms and entertainment.  It is a very popular stop for expats so you are bound to meet new friends with whom you can share stories and recommendations.  <br><br>
<h3><strong>Greater Accra</strong></h3><br>
When in Accra, make sure you visit <strong>Labadi Beach</strong>, a very popular spot on Wednesdays (Reggae Night) and on the weekends.  If you are interested in shopping and exercising your new negotiating skills, make your way to <strong>Makola Market</strong> and the <strong>Accra Art Centre</strong>.  These are massive markets where you can buy anything and everything.  <br>
On the way to Kasoa, just outside of Accra, you can enjoy a beautiful afternoon at <strong>Bojo Beach</strong> and <strong>Krokobite</strong>. Feel free to make it a weekend retreat and stay at <strong>Big Millys’</strong> backyard in Krokobite. <br><br> 
Just off the Accra-Aflao motorway, along the Volta River, you will find a nice beach town called <strong>Ada Foah</strong>. With the river on one side and the ocean on the other, Ada Foah is famous for water sports like swimming, sailing, fishing and boat cruises.
