<strong>Describe your typical day as a volunteer with DUNK:</strong>
The hours of operation of DUNK vary according to the time of year.  Since it is an organization that works closely and directly with children, our activities that involve their participation happen during non-school hours.<br><br>

Getting to work is part of the experience and is always an adventure.  Whether you negotiate for a taxi or take the trotro, transportation in Accra is never dull.  Once you have conquered the hectic commute, you will find yourself be instantly immersed into the magic that is DUNK when you turn the corner at the Nima based headquarters where the children will run up to you to greet you and help you with your bags.  These children are by far the best motivators one can ever experience.<br><br>

Your daily tasks will vary depending on your mandate, whether it is office work oriented or coaching. Regardless, the next 4-8 hours are dedicated to the most amazing children you will ever have the pleasure of interacting with. In all, by the end of your day, you will be fulfilled and satisfied of what you have accomplished and will look forward to seeing the children soon again.<br><br>

The Ghanaian nightlife is lively and exciting.  The appealing weather inspires outside activities, just be mindful of the mosquitoes!     Generally you can go and enjoy some local food and drinks at any “spot” and engage with locals while watching a football match.  Later on you will see people getting ready to go out dancing at one of the countless places in the city.  As you meet other expats and interact with the friendly locals, you will discover new favorite places and activities.<br><br>

<strong>What was the highlight of your trip?</strong>
My experience with DUNK has been transforming.  Getting to know the various volunteers and the amazing staff that share similar interests and values has been very valorizing.  However, the children and communities that I have had the honor of meeting and working with have definitely been the best part of my overall experience.  They are inspiring and great teachers themselves.  Their courage and hard work surpasses words and leaves you with a feeling that is indescribable.  These children are magical in that they are unstoppable in their pursuit of self-development and community engagement. Witnessing their daily struggles and challenges and all the while seeing their relentless perseverance in their participation in DUNK activities and programs because they value it and embrace the opportunities is inspiring and humbling to say the least.  You realize how special and unique this program is and how lucky you are to be able to be a part of it.<br><br>

</p>
