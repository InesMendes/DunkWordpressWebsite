<h3><strong>In General</strong></h3><br>
One thing you will notice in Ghana is the omnipresence of music and dance.  Should you be interested in experiencing a great night of dancing, you have come to the right place.  There is no shortage of clubs and dance halls (there are too many to name therefore feel free to ask any of the staff and your news friends for any recommendations!) Ghanaians love their nightlife, although they are generally not big consumers of alcohol.  Generally a typical night will start very late (around 11pm) and will finish when the last person leaves the dance floor (6am). Ghana has many more activities to offer. We suggest that you bring along a Travel Book, look at some tourism websites or ask our team for more activities. </p>
