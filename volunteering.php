<h3 style="padding-top:20px" align="left">MONEY MATTERS</h3>
<p align="left">There are plenty of places that will change currencies for you (Forex Bureau). The money used in Ghana is the Cedi. You can find 1, 2, 5, 10, 20, 50 cedis. The coins are called pesewa. You can find 1, 5, 10, 20 and 50 pesewas. Also, you can use credit and debit cards to withdrawal money in most of the ATMs. ATMs are fairly easy to find.
</p>
<h3 style="padding-top:20px" align="left">COMMUNICATING WITHIN AND OUTSIDE OF GHANA</h3>
<p align="left">While you are in Ghana, it is strongly recommended that you procure yourself a cell phone. There are numerous telecommunication companies and most offer great deals. The system is prepaid and you can buy credit everywhere. The credits are sold by 1 cedi, 2 cedis, 5 cedis and 10 cedis. Also, calling outside of Ghana from a Ghana phone is often less expensive then vice-versa.<br><br>As for Internet, there are several internet cafes but few places that offer Wi-Fi. However, if you intend on bringing your laptop, you can easily procure yourself with an Internet modem at reasonable prices. There are several telecommunication companies in Ghana (Vodafone, Glo, Expresso, Airtel and MTN). Similar to the phone credit, it is a prepaid system and you can purchase credit anywhere.
